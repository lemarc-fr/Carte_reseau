async function getOAuthToken(clientId, clientSecret) {
    const credentials = Buffer.from(`${clientId}:${clientSecret}`).toString("base64");

    const response = await fetch(
        "https://digital.iservices.rte-france.com/token/oauth/",
        {
            method: "POST",
            headers: {
                Authorization: `Basic ${credentials}`,
                "Content-Type": "application/x-www-form-urlencoded",
            },
            body: "grant_type=client_credentials",
        }
    );

    if (!response.ok) {
        throw new Error(
            `Auth failed: ${response.status} ${await response.text()}`
        );
    }

    const data = await response.json();
    return data.access_token;
}

async function getUnitProduction(token, eic, startDate, endDate) {
    const baseUrl =
        "https://digital.iservices.rte-france.com/open_api/actual_generation/v1/actual_generations_per_unit";

    const params = new URLSearchParams({
        start_date: startDate,
        end_date: endDate,
        unit_eic_code: eic,
    });

    const url = `${baseUrl}?${params.toString()}`;

    const response = await fetch(url, {
        headers: {
            Authorization: `Bearer ${token}`,
            Accept: "application/json",
        },
    });

    if (!response.ok) {
        throw new Error("Failed to fetch production data: " + response.status + " " + await response.text() + "")
    }

    return await response.json();
}

